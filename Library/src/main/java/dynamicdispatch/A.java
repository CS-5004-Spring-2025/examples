package dynamicdispatch;

/**
 * A class to demonstrate dynamic dispatch.
 * The abstract class A cannot be instantiated.
 *
 * Javadoc has been omitted for brevity.
 */
public abstract class A {

    public void m1() {
        System.out.println("A - m1");
    }

    public void m2() {
        System.out.println("A - m2");
    }

}

